    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
    combo_box.removeAllItems();
        try
       {
          Class.forName("java.sql.Driver");
          Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/test");
          Statement st=con.createStatement();
          String a="select train_no from train;";
          ResultSet rs=st.executeQuery(a);
         while (rs.next())
        {

        combo_box.addItem(rs.getString("train_no"));
        combo_box.setSelectedItem(rs.getString("train_no"));
        }
         rs.close();         
       }
        catch(Exception e)
       {
         JOptionPane.showMessageDialog(null,"ERROR IN CONNECTIVITY");
       }        
    }//GEN-LAST:event_formWindowActivated

    private void Seach_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Seach_buttonActionPerformed
    try
       {
          Class.forName("java.sql.Driver");
          Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/test");
          Statement st=con.createStatement();
          String tno=(String)combo_box.getSelectedItem();
          String a="select * from train where train_no='"+tno+"';";
          ResultSet rs=st.executeQuery(a);
          rs.first();
          String no=rs.getString(1);
          String name=rs.getString(2);
          String from=rs.getString(3);
          String to=rs.getString(4);
          String depature=rs.getString(5);
          String rent=rs.getString(6);
          String seats=rs.getString(7);
                    
          txtname.setText(name);
          txtfrom.setText(from);
          txtto.setText(to);
          txtd.setText(depature);
          txtrent.setText(rent);  
          txtseats.setText(seats); 
       }
       catch(Exception e)
       {
         JOptionPane.showMessageDialog(null,"NO TRAIN FOUND.");
       }
    }//GEN-LAST:event_Seach_buttonActionPerformed

