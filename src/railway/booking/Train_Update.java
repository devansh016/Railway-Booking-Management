    private void Upate_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Upate_buttonActionPerformed
        try
       {
          Class.forName("java.sql.Driver");
          Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/test");
          Statement st=con.createStatement();
          String no=(String) combo_box.getSelectedItem();
          String name=txtname.getText();
          String from=txtfrom.getText();
          String to=txtto.getText();
          String depature=txtd.getText();
          String rent=txtrent.getText();
          String seats=txtseats.getText();
          String a="update train set train_no='"+no+"',train_name='"+name+"',from_place='"+from+"',to_place='"
                  +to+"',depature='"+depature+"',rent='"+rent+"',seats='"+seats+"' where train_no='"+no+"';";
         st.executeUpdate(a);
         JOptionPane.showMessageDialog(null,"TRAIN UPDATED.");
         combo_box.removeAllItems();
       }
        catch(Exception e)
       {
         JOptionPane.showMessageDialog(null,"ERROR IN CONNECTIVITY");
       }      
    }//GEN-LAST:event_Upate_buttonActionPerformed

